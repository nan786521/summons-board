# Load monsters
with open("monsters.json", "r", encoding="utf-8") as f:
    monsters = json.load(f)

translated = 0
untranslated = set()

for m in monsters:
    if "tags" in m:
        new_tags = []
        for t in m["tags"]:
            if t in TAG_ZH:
                new_tags.append(TAG_ZH[t])
                translated += 1
            else:
                new_tags.append(t)
                untranslated.add(t)
        m["tags"] = new_tags

with open("monsters.json", "w", encoding="utf-8") as f:
    json.dump(monsters, f, ensure_ascii=False, indent=2)

print(f"已翻譯 {translated} 個標籤")
print(f"未翻譯: {len(untranslated)} 個")
for t in sorted(untranslated):
    print(f"  {t}")

def translate_text(text):
    """用字典做日文→中文替換"""
    if not text:
        return text
    # 按照詞長從長到短排序，避免短詞先匹配
    for ja, zh in sorted(DICT.items(), key=lambda x: -len(x[0])):
        text = text.replace(ja, zh)

    # 後處理：清理殘留日文助詞
    # W = CJK漢字 + 片假名 + 拉丁字母 + 數字
    W = r"[\u4E00-\u9FFF\u30A0-\u30FFa-zA-Z\d%]"
    # の → 的
    text = re.sub(rf"(?<={W})の(?={W})", "的", text)
    # が → 空
    text = re.sub(rf"(?<={W})が(?={W})", "", text)
    # を → 空
    text = re.sub(rf"(?<={W})を(?={W})", "", text)
    # は → 空
    text = re.sub(rf"(?<={W})は(?={W})", "", text)
    # に → 空（但保留在某些片假名前）
    text = re.sub(rf"(?<={W})に(?=[\u4E00-\u9FFF\d])", "", text)
    # で → 空
    text = re.sub(rf"(?<={W})で(?={W})", "", text)
    # 剩餘助詞清理
    text = text.replace("かつ", "且")
    text = text.replace("または", "或")
    text = text.replace("いずれか", "任一")
    text = text.replace("さらに", "再")
    text = text.replace("であれば", "時")
    text = text.replace("していれば", "時")
    text = text.replace("している", "中")
    text = text.replace("している", "中")
    text = text.replace("してから", "後")
    text = text.replace("しない", "不")
    text = text.replace("して", "")
    text = text.replace("する", "")
    text = text.replace("した", "")
    text = text.replace("される", "")
    text = text.replace("させる", "")
    text = text.replace("受ける", "承受")
    text = text.replace("与える", "造成")
    text = text.replace("すり抜け", "穿透")
    text = text.replace("なくなる", "消失")
    text = text.replace("なる", "變")
    text = text.replace("ない", "不")
    text = text.replace("なし", "無")
    text = text.replace("ある", "有")
    text = text.replace("いる", "中")
    text = text.replace("おり", "")
    text = text.replace("より", "比")
    text = text.replace("ごとに", "每")
    text = text.replace("おきの", "間隔的")
    text = text.replace("おき", "間隔")
    text = text.replace("のみ", "限定")
    text = text.replace("カット", "削減")
    text = text.replace("ずつ", "逐")
    text = text.replace("ごと", "每")

    return text


def translate_entry(entry):
    """翻譯一筆角色能力資料"""
    for skill_key in ["leader_skill", "active_skill", "tp_skill"]:
        if skill_key in entry:
            s = entry[skill_key]
            s["name"] = translate_text(s.get("name", ""))
            s["effect"] = translate_text(s.get("effect", ""))
            if "turn" in s:
                s["turn"] = translate_text(s["turn"])

    if "abilities" in entry:
        for a in entry["abilities"]:
            a["name"] = translate_text(a.get("name", ""))
            a["effect"] = translate_text(a.get("effect", ""))

    if "potentials" in entry:
        for p in entry["potentials"]:
            p["level"] = translate_text(p.get("level", ""))
            p["effect"] = translate_text(p.get("effect", ""))

    return entry


def main():
    with open("abilities.json", "r", encoding="utf-8") as f:
        data = json.load(f)

    print(f"載入 {len(data)} 筆角色能力資料")

    for key, entry in data.items():
        translate_entry(entry)

    with open("abilities.json", "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print("翻譯完成，已儲存 abilities.json")

    # 顯示翻譯前後範例
    sample_key = list(data.keys())[0]
    sample = data[sample_key]
    print(f"\n=== 範例: {sample.get('name')} ===")
    if "leader_skill" in sample:
        ls = sample["leader_skill"]
        print(f"隊長技: {ls['name']}")
        print(f"  效果: {ls['effect'][:100]}...")
    if "active_skill" in sample:
        a = sample["active_skill"]
        print(f"主動技: {a['name']}")
        print(f"  效果: {a['effect'][:100]}...")


if __name__ == "__main__":
    main()
